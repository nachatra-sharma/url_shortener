const mongoose = require("mongoose");

// define schema
const UserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// define user model

const User = mongoose.model("user", UserSchema);

module.exports = User;
